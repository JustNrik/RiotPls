namespace RiotPls.DataDragon.Enums
{
    public enum ChampionAbility
    {
        /// <summary>
        ///     Represents the passive ability of the champion.
        /// </summary>
        Passive,
        /// <summary>
        ///     Represents the first basic ability of the champion, which is by default binded to the key Q.
        /// </summary>
        Q,
        /// <summary>
        ///      Represents the second basic ability of the champion, which is by default binded to the key W.
        /// </summary>
        W,
        /// <summary>
        ///     Represents the third basic ability of the champion, which is by default binded to the key E.
        /// </summary>
        E,
        /// <summary>
        ///     Represents the ultimate ability of the champion, which is by default binded to the key R.
        /// </summary>
        R
    }
}
